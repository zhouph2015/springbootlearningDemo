package com.learning.model;

public class StringMessage {
	String message;
	
	public StringMessage() {
		super();
	}
	
	public StringMessage(String message) {
		super();
		this.message = message;
	}
	
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}

}
